/**
 * Represents type of controls
 */
export enum ControlType {
    /**
     * Container for other controls
     */
    ACTION_ROW = 1,

    /**
     * Buttin
     */
    BUTTON = 2,

    /**
     * Select menu or "combobox"
     */
    SELECT_MENU = 3,

    /**
     * Text input
     */
    TEXT_INPUT = 4
}